--server
if isClient() then return; end

local Commands = {};
Commands.myModule = {};

Commands.myModule.ismyModule = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('myModule', 'ismyModule', {id = playerId, ismyModule = args.ismyModule,  zedID = args.zedID})
end


Commands.myModule.knockDownZed = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('myModule', 'knockDownZed', {id = playerId, zedID = args.zedID})
end

Commands.myModule.isWearingmyModule = function(player, args)
    local playerId = player:dgetOnlineID();
    sendServerCommand('myModule', 'isWearingmyModule', {id = playerId, isWearingmyModule =  args.isWearingmyModule})
end


Commands.myModule.doSpawn = function(player, args)
    local x, y, z, count, fit, fChance, isDown = args.x,  args.y,  args.z, args.count, args.fit, args.fChance, args.isDown
    local zed = addZombiesInOutfit(round(x), round(y), round(z), 1, tostring(fit), tonumber(fChance), isDown, false, isDown, isDown, 1.0)
    if zed then
        if isDown then
            if not zed:isOnFloor() then
                zed:knockDown(true)
            end
        end
    end
end

Events.OnClientCommand.Add(function(module, command, player, args)
	if Commands[module] and Commands[module][command] then
	    Commands[module][command](player, args)
	end
end)


